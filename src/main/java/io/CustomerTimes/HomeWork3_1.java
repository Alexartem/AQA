package io.CustomerTimes;

public class HomeWork3_1 {

    public static void main(String[] args){

        for (int i = 10; i <= 20; i++) {
            System.out.println("for "+i);
        }
        int j = 10;


        while (j <= 20) {
            System.out.println("while "+j);
            j++;
        }

        int k = 10;
        do {
            k++;
            System.out.println("do while " + k);
        } while (k <= 20);
    }
}
